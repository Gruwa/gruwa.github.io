
/**
 * Interface for creating admins
 */

export interface IAdmin {

  /**
   * Required variable contain first name
   * @type {string}
   * @memberof IAdmin
   */

  first_name: string;

  /**
   * Required variable contain last name
   * @type {string}
   * @memberof IAdmin
   */

  last_name: string;

  /**
   * Required variable contain email
   * @type {string}
   * @memberof IAdmin
   */

  email: string;
}

